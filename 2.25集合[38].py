# 集合运算符
fruit_a = {'apple', 'orange', 'banana'}
fruit_b = {'grape', 'apple', 'cherry'}
fruit_c = {'grape', 'berry', 'cherry'}
print(fruit_a & fruit_b)
print(fruit_a & fruit_c)

fruit_a = {'apple', 'orange', 'banana'}
fruit_b = {'grape', 'apple', 'cherry'}
fruit_c = {'grape', 'berry', 'cherry'}
print(fruit_a | fruit_b)
print(fruit_a | fruit_b | fruit_c)
